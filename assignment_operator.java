package arithmetic;


public class assignment_operator {
     public static void main(String[] args) {

        int x=40;
        int y=20;
        //x++;
        x+=5;//also can write x=x+5;
        System.out.println("x="+x);
        
        x*=5;
        System.out.println("x="+x);
        
        x-=y;
        System.out.println("x="+x);
        
        x/=y;
        System.out.println("x="+x);

     }
}
